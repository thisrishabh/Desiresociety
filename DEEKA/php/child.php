	<p><font>Many non-profit organisations are working on issues pertaining to children. But by carving a niche for itself, Save the Children has by far emerged as one of the most committed and leading independent child rights non-profit organisation. As of December 2017, they were working in 19 states of India. It started in 2008 in India, and is registered as ‘Bal Raksha Bharat’. Till date, they have changed the lives of more than 10 million (1 crore) children. Last year, they reached 22.7 lakh children in need.

Save the Children believes that every child deserves a bright future. They are committed towards ensuring that children all across India should not only survive, but thrive. Save the Children is implementing programmes in the most remote corners and urban areas of the country. Their main aim is to provide quality education, healthcare, protection from harm and abuse, and life-saving aid during emergencies to children in need regardless of their caste, class, colour, race, religion, and ethnicity.

British Social Reformer Eglantyne Jebb started a movement for the betterment of children in need almost a century ago. This can be dated back to the post- First World War era.  The main aim of Save the Children Fund then was to provide the much-needed relief to children who had been impacted by the war. She drafted Declaration of Rights of the Child three years later and this formed the basis of the United Nations Convention on the Rights of Child (UNCRC)- the central premise of all child rights activities around the world.

Jebb believed that all children- whoever they are, wherever they are, have the right to a healthy, happy, and fulfilling life. Save the Children, India is working as an independent entity. They work for and with children in need. They are working effortlessly to provide every child with a happy childhood.

